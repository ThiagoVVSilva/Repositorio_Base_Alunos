# Importando a bibiblioteca pyautogui
import pyautogui

# 'press' e um comando q utilizamos para precionar a tecla 
pyautogui.press("win")

#sleep, e um comando q utilizamos para deixar o codigo em espera por alguns segundos
pyautogui.sleep(1)

#"write" e um comando que utilizamos para passar o que queresmos escrever
pyautogui.write("Google chrome")

pyautogui.press("Enter")
